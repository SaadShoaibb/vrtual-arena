// Generate metadata for a specific page
export const generatePageMetadata = (page, locale = 'en') => {
  const pageData = seoMetadata.pages[page] || seoMetadata.default;
  const localeData = pageData[locale] || pageData.en;
  
  return {
    title: localeData.title,
    description: localeData.description,
    keywords: localeData.keywords,
    openGraph: {
      title: localeData.title,
      description: localeData.description,
      url: `${seoMetadata.siteUrl}/${page === 'home' ? '' : page}?locale=${locale}`,
      siteName: seoMetadata.siteName,
      images: [
        {
          url: `${seoMetadata.siteUrl}/assets/logo.png`,
          width: 1200,
          height: 630,
          alt: localeData.title,
        },
      ],
      locale: locale === 'fr' ? 'fr_CA' : 'en_US',
      type: 'website',
    },
    twitter: {
      card: 'summary_large_image',
      title: localeData.title,
      description: localeData.description,
      images: [`${seoMetadata.siteUrl}/assets/logo.png`],
    },
    alternates: {
      canonical: `${seoMetadata.siteUrl}/${page === 'home' ? '' : page}`,
      languages: {
        'en-US': `${seoMetadata.siteUrl}/${page === 'home' ? '' : page}?locale=en`,
        'fr-CA': `${seoMetadata.siteUrl}/${page === 'home' ? '' : page}?locale=fr`,
      },
    },
  };
};

